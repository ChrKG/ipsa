#> solution
def window_average(k):
#< solution
#> validate arguments
    assert isinstance(k, int)
    assert k > 0 and k % 2 == 1
#< validate arguments
#> solution
    def decorator(f):
        def wrapper(x):
            x0 = x - k // 2
            return sum(f(y) for y in range(x0, x0 + k)) // k
        return wrapper
    return decorator
