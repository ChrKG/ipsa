#> solution
class Company:
    def __init__(self, name):
        self.name = name
        self.next_id = 0
    
class Employee:
    def __init__(self, name, company):
        self.name = name
        self.company = company
        company.next_id += 1
        self.id = company.next_id
    
    def __str__(self):
        return f'{self.name}, {self.company.name} (#{self.id})'
#< solution


